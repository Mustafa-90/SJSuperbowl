package com.example.Superbowl;

public class Tag {

    String tag1;
    String tag2;
    String tag3;
    String tag4;
    String tag5;


    public Tag() {

    }

    public Tag(String tag1, String tag2, String tag3, String tag4, String tag5) {
        this.tag1 = tag1;
        this.tag2 = tag2;
        this.tag3 = tag3;
        this.tag4 = tag4;
        this.tag5 = tag5;
    }

    public String getTag1() {
        return tag1;
    }

    public String getTag2() {
        return tag2;
    }

    public String getTag3() {
        return tag3;
    }

    public String getTag4() {
        return tag4;
    }

    public String getTag5() {
        return tag5;
    }
}
